/**
 * 按题型名称查找题型数据
 * @param {string} typeName - 题型名称
 * @returns {object|null}
 */
export function getQuestionType(typeName) {
  return QUESTION_TYPES.find(t => t.题型 === typeName) || null;
}

/**
 * 获取所有题型名称列表
 * @returns {string[]}
 */
export function getAllTypeNames() {
  return QUESTION_TYPES.map(t => t.题型);
}
